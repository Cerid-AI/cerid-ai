"""RAGAS evaluation harness -- continuous RAG quality measurement.

Evaluates retrieval and generation quality using standardized RAGAS metrics.
Requires: pip install -r requirements-eval.txt (ragas, scipy)

Baselines stored in tests/eval/baselines/ragas.json.
Target: Faithfulness >= 0.80, Context Precision >= 0.75.

Run: make test-eval
     python -m pytest tests/eval/ragas_eval.py -v
"""

from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import pytest

__all__ = ["load_golden_dataset", "evaluate_rag_quality", "save_baselines"]

_EVAL_DIR = Path(__file__).parent
_GOLDEN_PATH = _EVAL_DIR / "golden_dataset.json"
_BASELINES_PATH = _EVAL_DIR / "baselines" / "ragas.json"

GOLDEN_DATASET_SCHEMA = {
    "query": str,
    "ground_truth": str,
    "contexts": list,
}

_REQUIRED_FIELDS = set(GOLDEN_DATASET_SCHEMA.keys())


def _ragas_available() -> bool:
    """Check whether the ragas package is installed."""
    try:
        import ragas  # noqa: F401
        return True
    except ImportError:
        return False


def load_golden_dataset(path: Path | None = None) -> list[dict[str, Any]]:
    """Load the golden evaluation dataset from JSON.

    Each entry must contain: query, ground_truth, contexts.
    """
    p = path or _GOLDEN_PATH
    with p.open() as f:
        data = json.load(f)
    if not isinstance(data, list):
        msg = f"Golden dataset must be a JSON array, got {type(data).__name__}"
        raise TypeError(msg)
    return data


def evaluate_rag_quality(dataset: list[dict[str, Any]]) -> dict[str, float | None]:
    """Evaluate RAG quality using RAGAS metrics.

    When ragas is installed and a Cerid instance is running, this will:
    1. Send each query through the RAG pipeline.
    2. Collect retrieved contexts and generated answers.
    3. Compute faithfulness, context_precision, context_recall, answer_relevancy.
    4. Return a dict of metric_name -> score (0.0-1.0).

    Currently returns nulls -- wire up after golden dataset reaches 50 entries
    and RAGAS dependency is added to requirements-eval.txt.
    """
    return {
        "faithfulness": None,
        "context_precision": None,
        "context_recall": None,
        "answer_relevancy": None,
    }


def save_baselines(metrics: dict[str, float | None]) -> None:
    """Persist metric baselines to baselines/ragas.json."""
    payload = {
        "last_updated": datetime.now(timezone.utc).strftime("%Y-%m-%d"),
        "metrics": metrics,
        "notes": "Auto-generated by ragas_eval.py",
    }
    _BASELINES_PATH.parent.mkdir(parents=True, exist_ok=True)
    with _BASELINES_PATH.open("w") as f:
        json.dump(payload, f, indent=4)


# ---------------------------------------------------------------------------
# Pytest test class
# ---------------------------------------------------------------------------


class TestRAGASEvaluation:
    """Structural validation of the RAGAS evaluation scaffolding."""

    def test_golden_dataset_valid(self) -> None:
        """Load golden dataset and verify every entry has required fields."""
        dataset = load_golden_dataset()
        for i, entry in enumerate(dataset):
            missing = _REQUIRED_FIELDS - set(entry.keys())
            assert not missing, f"Entry {i} missing fields: {missing}"
            assert isinstance(entry["query"], str), f"Entry {i}: query must be str"
            assert isinstance(entry["ground_truth"], str), f"Entry {i}: ground_truth must be str"
            assert isinstance(entry["contexts"], list), f"Entry {i}: contexts must be list"

    def test_golden_dataset_minimum_size(self) -> None:
        """Golden dataset must have at least 10 entries."""
        dataset = load_golden_dataset()
        assert len(dataset) >= 10, f"Need >= 10 entries, got {len(dataset)}"

    def test_baseline_file_exists(self) -> None:
        """baselines/ragas.json must exist and be valid JSON with expected keys."""
        assert _BASELINES_PATH.exists(), f"Missing {_BASELINES_PATH}"
        with _BASELINES_PATH.open() as f:
            data = json.load(f)
        assert "metrics" in data, "Baseline file missing 'metrics' key"
        assert "last_updated" in data, "Baseline file missing 'last_updated' key"

    @pytest.mark.skipif(
        not _ragas_available(),
        reason="ragas not installed -- run pip install -r requirements-eval.txt",
    )
    def test_ragas_evaluation(self) -> None:
        """Run RAGAS evaluation on golden dataset.

        This test requires: pip install -r requirements-eval.txt
        And a running Cerid instance with populated KB.
        """
        pass  # Placeholder -- will be implemented when RAGAS is wired up
